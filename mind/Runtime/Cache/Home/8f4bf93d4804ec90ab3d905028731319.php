							<div class="col-sm-12 aa" style="padding-top: 30px;">
								<p style="text-indent: 2em; text-align: justify;">嫉妒，是人类一种常见的心理现象。每个人一生中都难免会有过嫉妒的念头，不过很多人并没有因此影响了自己的正常生活。可也有这样一部分人，他们的嫉妒心比较强，所以会因此而受到困扰。那么，嫉妒心理究竟有没有必要存在？如何调整自己的嫉妒心理？</p>
	<p style="text-indent: 2em;text-align: justify;">							
温某曾经是一名成功人士，因赌博欠债而变得生活潦倒。输光了前后，温某心理非常不平衡。据他自己交代，温某看到邻居家过的比他幸福，于是他就非常嫉妒，一下子冒出了杀人的念头。就在去年五月，温某给自己儿子两个玩伴的家长发信息，邀请两个孩子去他那里聚会。由于三家人走动一直很频繁，所以那两家的家长没有多想就把孩子送到了温某家中。谁料想，温某竟然丧心病狂的杀死了两个幼童。事后据温某自己交代，他和那两家人无冤无仇，就是嫉妒他们过得比自己幸福。</p>
	<p style="text-indent: 2em;text-align: justify;">	
看吧，这就是嫉妒心的危害。仅仅是出于嫉妒，就有人去破坏别人的劳动成果，就有人杀死别人家的小孩。其实，像易某和温某他们，这样做了之后自己就能幸福吗?当然不能，他们无非是用这种手段来发泄胸中的不满，这就是所谓的“损人不利己”。</p>
	<p style="text-indent: 2em;text-align: justify;">	
1、“嫉妒”会使人前进</p>
	<p style="text-indent: 2em;text-align: justify;">	
许多电影、电视剧的剧情由嫉妒推动，如宫廷剧《甄缳传》。美国作家约瑟夫·爱泼斯坦在《嫉妒》一书中写道：“骄傲和嫉妒不分彼此，自尊心受挫会导致嫉妒，正如失败后会心生恨意。”</p>
	<p style="text-indent: 2em;text-align: justify;">	
现代社会强调竞争，加上社交媒体的崛起，我们时时刻刻都能“窥探”到别人的生活。谁都不免去想：“我要是也有那么一辆好车、能住那么大的房子、能全球旅行就好了。”我们甚至可能愤怒：为什么他们拥有那么多好东西——美貌、地位、权力、爱情，我的那份呢?</p>
	<p style="text-indent: 2em;text-align: justify;">	
论语有云：“君子周而不比，小人比而不周。”嫉妒的产生与心理落差密切相关。人们常用“气人有，笑人无”来比喻某些爱嫉妒的人。当现实与理想存在差距时，人会攀比，内心产生严重的不平衡感及对他人的反感，造成人际关系紧张。嫉妒还与性格关系密切。一般来说，低自尊者的嫉妒心往往更强，高自尊者的嫉妒心一般较弱。</p>
	<p style="text-indent: 2em;text-align: justify;">	
嫉妒的核心其实就是一句话：我不够好或我不如别人好。人和人就是相互照镜子，在一面“好”的镜子中，照出了自己“不够好”，那样的痛苦会直接激发“被抛弃，被忽略”的恐惧感。</p>
	<p style="text-indent: 2em;text-align: justify;">	
然而，嫉妒是一种正常的情感体验，包含消极的力量(如憎恨、攻击等)和积极的力量(如强烈的占有欲和拥有欲)。嫉妒让人痛苦，是因为被恨意和攻击占用了大部分情绪。若把这些消极力量化为渴望和拥有，就能化嫉妒为动力。</p>
	<p style="text-indent: 2em;text-align: justify;">	
要克服自己的嫉妒心理，首先问问自己：我比较的东西是什么?我究竟想要什么?别人拥有的并不一定是自己想要的，要学会接纳、尊重、欣赏和换位思考。其次要寻找差距迎头赶上。一个人嫉妒什么，就意味他的自卑感在哪里，也意味着自尊心在哪里，同时意味着追求的目标在哪里。客观分析自身情况，认真反思别人的优势所在，自己需要怎么做才能达到那种水平。将注意力放在丰富自身知识、提高自身能力上，而不是靠嫉妒把人拉下马。最后，用正当的方式宣泄情绪，如找家人或朋友倾诉;通过运动排解负面情绪等。更要看到自身拥有的长处，发掘自身潜力，学会尊重自己、爱自己。</p>
	<p style="text-indent: 2em;text-align: justify;">	
2、测测你的嫉妒指数</p>
	<p style="text-indent: 2em;text-align: justify;">	
这个关于嫉妒的小测试来自美国加州大学洛杉矶分校精神心理学家朱迪斯·欧洛芙博士的《情感自由》一书。</p>
	<p style="text-indent: 2em;text-align: justify;">	
用“是”或“否”回答下列问题。</p>
	<p style="text-indent: 2em;text-align: justify;">	
(1)我是否以贬低他人为乐?</p>
	<p style="text-indent: 2em;text-align: justify;">	
(2)尽管我喜欢某位同事，但他或她得不到升迁，我是否觉得轻松许多?</p>
	<p style="text-indent: 2em;text-align: justify;">	
(3)我是否觉得他人比自己强?</p>
	<p style="text-indent: 2em;text-align: justify;">	
(4)我是否憎恨那些看起来什么都有的人?</p>
	<p style="text-indent: 2em;text-align: justify;">	
(5)我是否觉得自己在别人的美貌、智慧和成就面前相形见绌?</p>
	<p style="text-indent: 2em;text-align: justify;">
(6)我是否害怕别人比我好?</p>
	<p style="text-indent: 2em;text-align: justify;">
(7)配偶过去的情人会不会对我构成威胁?</p>
	<p style="text-indent: 2em;text-align: justify;">
(8)我是否把配偶的朋友看成威胁?</p>
	<p style="text-indent: 2em;text-align: justify;">
如果回答“是”的个数大于等于6个，说明你的嫉妒心很强;3~5个说明你的嫉妒心一般;小于等于两个，说明你不是个爱嫉妒的人;全回答“否”，说明你的嫉妒心微乎其微，但也可能是在自欺欺人。</p>
	<p style="text-indent: 2em;text-align: justify;">
心理解读：想克服嫉妒心，最好的方法还是升华，最重要有自信心和拼搏的精神，即把嫉妒升华为自己赶超的动力向上的动力。这也是嫉妒心的一种好处，它能让人不断地进步。就像是体育比赛，嫉妒心可以促使大家达到“更高更快更强”，这也是提升自我的一种方式。当人能把嫉妒变成动力时，也是一种互相提高互相进步的过程嫉妒不总是恶意的.</p>
